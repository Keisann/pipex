#include"libft.h"

char	*ft_strdup(const char *src)
{
	char		*dest;
	size_t		i;
	size_t		len;

	len = ft_strlen(src);
	i = 0;
	if (src == NULL)
		return (NULL);
	dest = malloc(sizeof(char) * (len + 1));
	if (dest == NULL)
		return (NULL);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
