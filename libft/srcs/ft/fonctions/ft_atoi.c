#include"libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	total;
	int	s;

	i = 0;
	total = 0;
	s = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s *= -1;
		i++;
	}
	while (str[i] && '0' <= str[i] && str[i] <= '9')
	{
		total *= 10;
		total += (str[i] - '0');
		i++;
	}
	return (total * s);
}
